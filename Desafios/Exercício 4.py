#Programa criado para cacular o perímetro e a área de qualquer retângulo após os valores da base e altura serem digitados pelo usuário.


base = float (input("Digite a base do retângulo: "))
altura = float (input("Digite a altura do retângulo: "))
print("O perímetro do retângulo é: ", 2 * (base + altura))
print("A área do retângulo é: ", base * altura)
