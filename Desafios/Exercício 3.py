#Prograa criado para leitura de um número inteiro e posteriomente o próprio número, seu sucessor e seu antecessor serem exibidos na tela

num = int (input("Digite um número inteiro: "))
print("O número digitado foi: ", num)
print("O sucessor do numero digitado é: ", num + 1)
print("O antecessor do número digitado é: ", num - 1)